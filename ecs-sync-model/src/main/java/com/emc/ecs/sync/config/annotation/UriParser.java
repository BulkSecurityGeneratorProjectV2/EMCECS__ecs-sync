package com.emc.ecs.sync.config.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotates a method of a @StorageConfig that is responsible for parsing the URI provided by the CLI (via --source or
 * --target). this should set other config properties as the original URI should not be referenced directly. This
 * method must have a void return type and a single String parameter.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface UriParser {
}
