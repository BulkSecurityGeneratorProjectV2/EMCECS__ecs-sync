package com.emc.ecs.sync.config.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface StorageConfig {
    /**
     * The URI prefix that denotes a particular storage type (plugin) in the command line --source or --target URI
     * parameters. For example. if the uriPrefix is <code>file:</code>, then the parameter
     * <code>--source file:...</code> would be parsed by this storage config and its associated plugin.
     */
    String uriPrefix();
}
